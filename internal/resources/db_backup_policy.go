// dbpCreateBody builds the body for CREATE (all required fields sent).
func dbpCreateBody(plan dbBackupPolicyModel) map[string]any {
	body := map[string]any{
		"name":                       plan.Name.ValueString(),
		"s3_endpoint":                plan.S3Endpoint.ValueString(),
		"s3_bucket":                  plan.S3Bucket.ValueString(),
		"s3_region":                  plan.S3Region.ValueString(),
		"s3_access_key":              plan.S3AccessKey.ValueString(),
		"s3_secret_key":              plan.S3SecretKey.ValueString(),
		"full_backup_frequency":      plan.FullBackupFrequency.ValueString(),
		"full_backup_time":           plan.FullBackupTime.ValueString(),
		"incremental_frequency":      plan.IncrementalFrequency.ValueString(),
		"retention_full_count":       plan.RetentionFullCount.ValueInt64(),
		"retention_incremental_days": plan.RetentionIncrementalDays.ValueInt64(),
		"retention_pitr_hours":       plan.RetentionPitrHours.ValueInt64(),
	}
	if !plan.S3PathPrefix.IsNull() && !plan.S3PathPrefix.IsUnknown() {
		body["s3_path_prefix"] = plan.S3PathPrefix.ValueString()
	}
	if !plan.FullBackupDay.IsNull() && !plan.FullBackupDay.IsUnknown() {
		body["full_backup_day"] = plan.FullBackupDay.ValueInt64()
	}
	if !plan.PitrEnabled.IsNull() && !plan.PitrEnabled.IsUnknown() {
		body["pitr_enabled"] = plan.PitrEnabled.ValueBool()
	}
	if !plan.EncryptionEnabled.IsNull() && !plan.EncryptionEnabled.IsUnknown() {
		body["encryption_enabled"] = plan.EncryptionEnabled.ValueBool()
	}
	return body
}

// dbpUpdateBody builds the body for UPDATE. Credentials are only sent when
// the plan value differs from state (prevents blanking out stored credentials
// when the user has not changed them). The server strips empty credential
// values, but we are explicit to match the service logic.
func dbpUpdateBody(plan, state dbBackupPolicyModel) map[string]any {
	body := map[string]any{
		"name":                       plan.Name.ValueString(),
		"s3_endpoint":                plan.S3Endpoint.ValueString(),
		"s3_bucket":                  plan.S3Bucket.ValueString(),
		"s3_region":                  plan.S3Region.ValueString(),
		"full_backup_frequency":      plan.FullBackupFrequency.ValueString(),
		"full_backup_time":           plan.FullBackupTime.ValueString(),
		"incremental_frequency":      plan.IncrementalFrequency.ValueString(),
		"retention_full_count":       plan.RetentionFullCount.ValueInt64(),
		"retention_incremental_days": plan.RetentionIncrementalDays.ValueInt64(),
		"retention_pitr_hours":       plan.RetentionPitrHours.ValueInt64(),
	}
	if !plan.S3PathPrefix.IsNull() && !plan.S3PathPrefix.IsUnknown() {
		body["s3_path_prefix"] = plan.S3PathPrefix.ValueString()
	}
	if !plan.FullBackupDay.IsNull() && !plan.FullBackupDay.IsUnknown() {
		body["full_backup_day"] = plan.FullBackupDay.ValueInt64()
	}
	if !plan.PitrEnabled.IsNull() && !plan.PitrEnabled.IsUnknown() {
		body["pitr_enabled"] = plan.PitrEnabled.ValueBool()
	}
	if !plan.EncryptionEnabled.IsNull() && !plan.EncryptionEnabled.IsUnknown() {
		body["encryption_enabled"] = plan.EncryptionEnabled.ValueBool()
	}
	// Only send credentials if they changed (non-empty and different from prior
	// state). This matches the service behaviour of stripping empty credential
	// fields, and avoids accidental credential blanking on unrelated updates.
	if !plan.S3AccessKey.Equal(state.S3AccessKey) && plan.S3AccessKey.ValueString() != "" {
		body["s3_access_key"] = plan.S3AccessKey.ValueString()
	}
	if !plan.S3SecretKey.Equal(state.S3SecretKey) && plan.S3SecretKey.ValueString() != "" {
		body["s3_secret_key"] = plan.S3SecretKey.ValueString()
	}
	return body
}

// readState GETs the policy and builds a full model from it. prior supplies
// fallbacks for fields the response omits (especially the write-only
// credentials that the model hides). This is the KEY preservation point:
// s3_access_key and s3_secret_key are copied from prior state because SHOW
// never returns them.
func (r *dbBackupPolicyResource) readState(ctx context.Context, id string, prior dbBackupPolicyModel) (dbBackupPolicyModel, bool, diag.Diagnostics) {
	obj, err := r.client.GetDBBackupPolicy(ctx, id)
	if err != nil {
		if client.IsNotFound(err) {
			return dbBackupPolicyModel{}, true, nil
		}
		var diags diag.Diagnostics
		diags.Append(diagFromErr("Error reading database backup policy", err))
		return dbBackupPolicyModel{}, false, diags
	}
	m, diags := dbpStateFromAPI(obj, prior)
	return m, false, diags
}

// dbpStateFromAPI builds the model from the SHOW "policy" object. Write-only
// credential fields are preserved from the prior state (not overwritten).
func dbpStateFromAPI(obj map[string]any, prior dbBackupPolicyModel) (dbBackupPolicyModel, diag.Diagnostics) {
	var diags diag.Diagnostics

	m := dbBackupPolicyModel{
		ID:                       stringFromAPI(obj, "id", prior.ID),
		Name:                     stringFromAPI(obj, "name", prior.Name),
		S3Endpoint:               stringFromAPI(obj, "s3_endpoint", prior.S3Endpoint),
		S3Bucket:                 stringFromAPI(obj, "s3_bucket", prior.S3Bucket),
		S3Region:                 stringFromAPI(obj, "s3_region", prior.S3Region),
		FullBackupFrequency:      stringFromAPI(obj, "full_backup_frequency", prior.FullBackupFrequency),
		FullBackupTime:           stringFromAPI(obj, "full_backup_time", prior.FullBackupTime),
		FullBackupDay:            optionalInt64FromAPI(obj, "full_backup_day"),
		IncrementalFrequency:     stringFromAPI(obj, "incremental_frequency", prior.IncrementalFrequency),
		RetentionFullCount:       requiredInt64FromAPI(obj, "retention_full_count", prior.RetentionFullCount),
		RetentionIncrementalDays: requiredInt64FromAPI(obj, "retention_incremental_days", prior.RetentionIncrementalDays),
		RetentionPitrHours:       requiredInt64FromAPI(obj, "retention_pitr_hours", prior.RetentionPitrHours),
		Status:                   stringFromAPI(obj, "status", prior.Status),

		// Credentials are $hidden by the model - preserve prior values so Terraform
		// does not see spurious drift and does not blank them on the next apply.
		S3AccessKey: prior.S3AccessKey,
		S3SecretKey: prior.S3SecretKey,
	}

	// Optional path prefix: map empty/absent to null so removing the prefix in
	// config clears it in state rather than perpetually falling back to prior.
	if v, ok := obj["s3_path_prefix"].(string); ok && v != "" {
		m.S3PathPrefix = types.StringValue(v)
	} else {
		m.S3PathPrefix = types.StringNull()
	}

	// Boolean fields: fall back to prior when absent.
	if v, ok := obj["pitr_enabled"].(bool); ok {
		m.PitrEnabled = types.BoolValue(v)
	} else {
		m.PitrEnabled = prior.PitrEnabled
	}
	if v, ok := obj["encryption_enabled"].(bool); ok {
		m.EncryptionEnabled = types.BoolValue(v)
	} else {
		m.EncryptionEnabled = prior.EncryptionEnabled
	}

	// Rebuild database_ids from the embedded "managed_databases" array.
	dbSet, d := dbIDSetFromAPI(obj["managed_databases"], prior.DatabaseIDs)
	diags.Append(d...)
	m.DatabaseIDs = dbSet

	return m, diags
}

// dbIDSetFromAPI converts the embedded "managed_databases" array (each element
// has an "id" field) into a types.Set of id strings. The logic mirrors
// instanceIDSetFromAPI from security_group.go.
func dbIDSetFromAPI(raw any, prior types.Set) (types.Set, diag.Diagnostics) {
	arr, _ := raw.([]any)
	if len(arr) == 0 {
		if prior.IsNull() || prior.IsUnknown() {
			return types.SetNull(types.StringType), nil
		}
		return types.SetValue(types.StringType, []attr.Value{})
	}

	elems := make([]attr.Value, 0, len(arr))
	for _, item := range arr {
		io, ok := item.(map[string]any)
		if !ok {
			continue
		}
		if idv, ok := io["id"].(string); ok && idv != "" {
			elems = append(elems, types.StringValue(idv))
		}
	}
	if len(elems) == 0 {
		if prior.IsNull() || prior.IsUnknown() {
			return types.SetNull(types.StringType), nil
		}
		return types.SetValue(types.StringType, []attr.Value{})
	}
	return types.SetValue(types.StringType, elems)
}
