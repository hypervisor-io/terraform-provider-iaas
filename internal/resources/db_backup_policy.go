// dbBackupPolicyResource manages an iaas_db_backup_policy - an off-host S3
// backup configuration for managed databases, with schedule, PITR, and
// retention settings.
//
// The attached database set is managed as an Optional SetAttribute of strings
// (managed_database UUIDs), diffed via one-at-a-time attach/detach calls on
// update, and rebuilt from the embedded "policy.managed_databases" array on
// Read. This follows the same set-diff pattern as iaas_instance_backup_policy,
// adapted for databases (managed_database_id in the request body).
//
// Route summary (verified against UserApi\DbBackupPolicyController +
// DbBackupPolicyService + DbPolicyStoreRequest/UpdateRequest/AttachRequest +
// routes/user_api.php):
//
//	INDEX   GET    /networking/db-backup-policies              (plural)
//	CREATE  POST   /networking/db-backup-policies              (plural)
//	                body {name,s3_endpoint,s3_bucket,s3_region,
//	                      s3_access_key,s3_secret_key,s3_path_prefix?,
//	                      full_backup_frequency,full_backup_time,
//	                      full_backup_day?,incremental_frequency,
//	                      pitr_enabled?,retention_full_count,
//	                      retention_incremental_days,retention_pitr_hours,
//	                      encryption_enabled?}
//	                → {success,message,policy:{id,...}}
//	SHOW    GET    /networking/db-backup-policy/{id}           (singular)
//	                → {policy:{...,managed_databases:[{id,...}]},
//	                   available_databases:[...]}
//	                NOTE: s3_access_key,s3_secret_key,encryption_key are
//	                $hidden on the model - never returned by SHOW.
//	UPDATE  PATCH  /networking/db-backup-policy/{id}          (singular)
//	                body same as CREATE (all "sometimes|required")
//	                → {success,message,policy:{...}}
//	DELETE  DELETE /networking/db-backup-policy/{id}          (singular)
//	                → {success,message}
//	ATTACH  POST   /networking/db-backup-policy/{id}/attach   body {managed_database_id}
//	DETACH  POST   /networking/db-backup-policy/{id}/detach   body {managed_database_id}
//
// KEY FINDING - WRITE-ONLY CREDENTIALS:
//   - s3_access_key, s3_secret_key are $hidden on the DbBackupPolicy model.
//     SHOW never returns them. They are sent on Create (required) and
//     optionally on Update (empty values are stripped by the service,
//     preserving the stored credentials). The provider must preserve these
//     values in state between reads (they cannot be recovered from SHOW).
//     They are marked Sensitive + the resource's Read does NOT overwrite them.
//   - encryption_key is auto-generated by the server when encryption is first
//     enabled; it is also hidden and cannot be recovered. Not exposed.
//
// All operations are SYNCHRONOUS from the API's perspective (attach dispatches
// a slave job but the HTTP response returns immediately). No billing gate.
// testConnection and reset-failures are NOT modelled (operational).
type dbBackupPolicyResource struct {
	client *client.Client
}

