// Create provisions the backup policy and then attaches the configured
// database ids one by one, then reads back to get authoritative state.
func (r *dbBackupPolicyResource) Create(ctx context.Context, req resource.CreateRequest, resp *resource.CreateResponse) {
	var plan dbBackupPolicyModel
	resp.Diagnostics.Append(req.Plan.Get(ctx, &plan)...)
	if resp.Diagnostics.HasError() {
		return
	}

	body := dbpCreateBody(plan)

	obj, err := r.client.CreateDBBackupPolicy(ctx, body)
	if err != nil {
		resp.Diagnostics.Append(diagFromErr("Error creating database backup policy", err))
		return
	}

	id, _ := obj["id"].(string)
	if id == "" {
		resp.Diagnostics.AddError("Error creating database backup policy",
			"the create response did not contain an id")
		return
	}

	// Persist a minimal destroyable state before side-effects.
	persistMinimal := func() {
		_ = resp.State.Set(ctx, dbBackupPolicyModel{
			ID:                       types.StringValue(id),
			Name:                     plan.Name,
			S3Endpoint:               plan.S3Endpoint,
			S3Bucket:                 plan.S3Bucket,
			S3Region:                 plan.S3Region,
			S3AccessKey:              plan.S3AccessKey,
			S3SecretKey:              plan.S3SecretKey,
			S3PathPrefix:             plan.S3PathPrefix,
			FullBackupFrequency:      plan.FullBackupFrequency,
			FullBackupTime:           plan.FullBackupTime,
			FullBackupDay:            plan.FullBackupDay,
			IncrementalFrequency:     plan.IncrementalFrequency,
			PitrEnabled:              plan.PitrEnabled,
			RetentionFullCount:       plan.RetentionFullCount,
			RetentionIncrementalDays: plan.RetentionIncrementalDays,
			RetentionPitrHours:       plan.RetentionPitrHours,
			EncryptionEnabled:        plan.EncryptionEnabled,
			Status:                   types.StringValue("active"),
			DatabaseIDs:              types.SetNull(types.StringType),
		})
	}

	// Attach the configured databases one at a time.
	attachIDs, diags := stringsFromSet(ctx, plan.DatabaseIDs)
	resp.Diagnostics.Append(diags...)
	if resp.Diagnostics.HasError() {
		return
	}
	for _, dbID := range attachIDs {
		if err := r.client.AttachDatabaseToBackupPolicy(ctx, id, dbID); err != nil {
			resp.Diagnostics.Append(diagFromErr("Error attaching database to backup policy", err))
			persistMinimal()
			return
		}
	}

	// Read back so state reflects the server-assigned values.
	state, notFound, diags := r.readState(ctx, id, plan)
	resp.Diagnostics.Append(diags...)
	if notFound {
		resp.Diagnostics.AddError("Error creating database backup policy",
			"the policy disappeared immediately after creation")
		return
	}
	if resp.Diagnostics.HasError() {
		return
	}
	resp.Diagnostics.Append(resp.State.Set(ctx, state)...)
}

// Read refreshes state from the API. A 404 means the policy was deleted
// out of band - remove it from state so Terraform plans a recreate.
func (r *dbBackupPolicyResource) Read(ctx context.Context, req resource.ReadRequest, resp *resource.ReadResponse) {
	var state dbBackupPolicyModel
	resp.Diagnostics.Append(req.State.Get(ctx, &state)...)
	if resp.Diagnostics.HasError() {
		return
	}

	newState, notFound, diags := r.readState(ctx, state.ID.ValueString(), state)
	if notFound {
		resp.State.RemoveResource(ctx)
		return
	}
	resp.Diagnostics.Append(diags...)
	if resp.Diagnostics.HasError() {
		return
	}
	resp.Diagnostics.Append(resp.State.Set(ctx, newState)...)
}

// Update applies the planned changes: patches all fields, then diffs the
// database_ids set (attach added / detach removed).
func (r *dbBackupPolicyResource) Update(ctx context.Context, req resource.UpdateRequest, resp *resource.UpdateResponse) {
	var plan, state dbBackupPolicyModel
	resp.Diagnostics.Append(req.Plan.Get(ctx, &plan)...)
	resp.Diagnostics.Append(req.State.Get(ctx, &state)...)
	if resp.Diagnostics.HasError() {
		return
	}

	id := state.ID.ValueString()

	// PATCH the scalar fields.
	body := dbpUpdateBody(plan, state)
	if _, err := r.client.UpdateDBBackupPolicy(ctx, id, body); err != nil {
		resp.Diagnostics.Append(diagFromErr("Error updating database backup policy", err))
		return
	}

	// Diff the database_ids set: attach added, detach removed.
	plannedIDs, diags := stringsFromSet(ctx, plan.DatabaseIDs)
	resp.Diagnostics.Append(diags...)
	stateIDs, diags := stringsFromSet(ctx, state.DatabaseIDs)
	resp.Diagnostics.Append(diags...)
	if resp.Diagnostics.HasError() {
		return
	}

	toAttach, toDetach := ibpDiffIDs(plannedIDs, stateIDs)

	for _, dbID := range toAttach {
		if err := r.client.AttachDatabaseToBackupPolicy(ctx, id, dbID); err != nil {
			resp.Diagnostics.Append(diagFromErr("Error attaching database to backup policy", err))
			return
		}
	}
	for _, dbID := range toDetach {
		if err := r.client.DetachDatabaseFromBackupPolicy(ctx, id, dbID); err != nil {
			resp.Diagnostics.Append(diagFromErr("Error detaching database from backup policy", err))
			return
		}
	}

	// Read back so state is authoritative.
	newState, notFound, diags := r.readState(ctx, id, plan)
	resp.Diagnostics.Append(diags...)
	if notFound {
		resp.Diagnostics.AddError("Error updating database backup policy",
			"the policy disappeared during update")
		return
	}
	if resp.Diagnostics.HasError() {
		return
	}
	resp.Diagnostics.Append(resp.State.Set(ctx, newState)...)
}

// Delete removes the backup policy (the service detaches all databases first).
func (r *dbBackupPolicyResource) Delete(ctx context.Context, req resource.DeleteRequest, resp *resource.DeleteResponse) {
	var state dbBackupPolicyModel
	resp.Diagnostics.Append(req.State.Get(ctx, &state)...)
	if resp.Diagnostics.HasError() {
		return
	}

	if err := r.client.DeleteDBBackupPolicy(ctx, state.ID.ValueString()); err != nil {
		resp.Diagnostics.Append(diagFromErr("Error deleting database backup policy", err))
		return
	}
}

